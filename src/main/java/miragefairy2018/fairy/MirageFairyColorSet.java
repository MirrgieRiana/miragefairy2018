package miragefairy2018.fairy;

public class MirageFairyColorSet
{

	public final int skin;
	public final int darker;
	public final int brighter;
	public final int hair;

	public MirageFairyColorSet(int skin, int darker, int brighter, int hair)
	{
		this.skin = skin;
		this.darker = darker;
		this.brighter = brighter;
		this.hair = hair;
	}

}
